public class Images {
    private String id;
    private String classified_id;
    private String image;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClassified_id() {
        return classified_id;
    }

    public void setClassified_id(String classified_id) {
        this.classified_id = classified_id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
